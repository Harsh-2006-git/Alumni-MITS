export const getCityCoordinates = (cityName) => {
    // Direct match
    if (cityCoordinates[cityName]) return cityCoordinates[cityName];

    // Case-insensitive match
    const lowerCityName = cityName.toLowerCase();
    const match = Object.keys(cityCoordinates).find(
        key => key.toLowerCase() === lowerCityName
    );

    return match ? cityCoordinates[match] : null;
};
